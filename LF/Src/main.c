void PWM(uint16_t r, uint16_t l)
{
	if(r>=0)
	{
		if(r>100)
			r=100;
		Set(SIL1A_GPIO_Port, SIL1A_Pin, 1);
		Set(SIL1B_GPIO_Port, SIL1B_Pin, 0);
	}
	else
	{
		if(r<-100)
			r=-100;
		r=-r;
		Set(SIL1A_GPIO_Port, SIL1A_Pin, 0);
		Set(SIL1B_GPIO_Port, SIL1B_Pin, 1);
	}

	if(l>=0)
	{
		if(l>100)
			l=100;
		Set(SIL2A_GPIO_Port, SIL2A_Pin, 1);
		Set(SIL2B_GPIO_Port, SIL2B_Pin, 0);
	}
	else
	{
		if(l<-100)
			l=-100;
		l=-l;
		Set(SIL2A_GPIO_Port, SIL2A_Pin, 0);
		Set(SIL2B_GPIO_Port, SIL2B_Pin, 1);
	}

	TIM3->CCR1 = r;
	TIM3->CCR2 = l;

}

void kalibracja()
{

	int bialy = ktiry[0];
	int czarny = ktiry[3];

	prog = bialy+(czarny-bialy)/2;

}

void odczyt()
{
	for(int i=0;i<4;i++)
		if(ktiry[i] > prog)
			ktiry[i] = 1;
		else ktiry[i] = 0;
}

int16_t blad()
{
	int pom = 0;
	int b = 0;
	int w = 0;
	int wagi[4] = {-20, 0, 10, 40};

	if(wyjechal) w = 5;

	for(int i=0;i<4;i++)
	{
		if(wyjechal)
			b+=ktiry[i]*w*(i-1);
		else b+=ktiry[i]*wagi[i]*(i-1);
		pom+=ktiry[i];
	}

	if(pom!=0)
	{
		b /= pom;
		err = b;
	}
	else
	{
		if(err < -20)
		{
			b = -40;
			wyjechal = 1;
		}
		else if(err > 20)
		{
			b = 30;
			wyjechal = 2;
		}
		else b = 0;
	}
	if((wyjechal == 1 && b >= 0)||(wyjechal == 2 && b <= 0))
		wyjechal = 0;

	return b;
}

int PD()
{
	int r = bledzik - popbledzik;
	popbledzik = bledzik;
	return Kp*bledzik+Kd*r;
}


